/** Get templates filtered by vocational group */
export function getTemplatesByGroup(group: VocationalGroup): CapstoneTemplate[] {
  return CAPSTONE_TEMPLATES.filter(t => t.group === group);
}

/** Get a single template by ID */
export function getTemplateById(id: string): CapstoneTemplate | undefined {
  return CAPSTONE_TEMPLATES.find(t => t.id === id);
}
